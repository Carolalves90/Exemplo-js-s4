for(let contador = 1; contador <= 50; contador++) {
    console.log(`Pulei a corda com o for ${contador}x`)
}

// contagem regressiva
for(let contador = 50; contador >= 1; contador--) {
    console.log(`Pulei a corda com o for ${contador}x`)
}

// contagem regressiva de 3 em 3
for(let contador = 50; contador >= 1; contador = contador - 3) {
    console.log(`Pulei a corda com o for ${contador}x`)
}

// contagem de 5 em 5 com código de contagem simples
for (let contador = 5; contador <= 50; contador+=5){
    console.log("Número" + contador )
}
